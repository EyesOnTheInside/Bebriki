enum SymbolicTokenClass { Alpha, Digit, AR_OP, Bool, Dot, Left_bracket, Right_braket, Space, EndOfString, EndOfFile, Error, END, FOR, GOSUB, GOTO, IF, LET, NEXT, REM, RETURN, STEP, TO };
enum States { A1, A2, A3, B1, C1, C2, D1, D2, D3, D4, D5, D6, E1, E2, F1, F2, F3, G1, H1, Stop };

struct LexemToken
{
    SymbolicTokenClass token_class;
    std::string reg_data;
    int number_reg; // регистр числа
    std::string relat_reg;// регистр отношения
    std::string variable_reg;// регистр переменной
    int detect_reg;// регистр обнаружения
    std::string token_reg;// регистр лексемы
    int reg_str = 1;// номер строки

    void clear()
    {
        number_reg = 0;
        reg_data.clear();
        relat_reg.clear();
        variable_reg.clear();
        detect_reg = 0;
        token_reg.clear();
    }

};

struct Token {
    int m_line;
    std::string lexem_type;
    std::string lexem_name;

    Token(int line, std::string type, std::string name) {
        m_line = line;
        lexem_type = type;
        lexem_name = name;
    }

};

